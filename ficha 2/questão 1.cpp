#include <iostream>
#include <cmath>
using namespace std;

int main() {
    
    float x, y, z;
    float T = 7;
    
    
    x = (T * 7 + 3 * 6) / 2 - 1;
    
    
    y = 2 % 2 + 2 * 2 / 2;
    
    
    z = (3 * 8 * 9 * (3 + (9 * 3 / 3)));

    
    cout << "Valor de x: " << x << endl;
    cout << "Valor de y: " << y << endl;
    cout << "Valor de z: " << z << endl;

    return 0;
}
